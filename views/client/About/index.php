		<h2>History of sneaker shoes</h2>
		<p>Rubber soles were invented by Charles Goodyear. He researched and discovered that the vulcanization process
			of rubber in 1839 made it extremely good to bond with other materials such as cloth.
			By 1870, the first sneaker was produced and was known as the Plimsoll shoe. Plimsoll shoes are designed
			primarily for beach use and have a horizontal stripe on the sole. This line is likened to the plimsoll line
			on a ship, if you step on the water and the water crosses the line, your shoes will get wet.</p>
		<img src="https://bizweb.dktcdn.net/100/347/092/files/giay-sneaker-la-gi-3.jpg?v=1599104036016" alt=""
			style=" padding-left: 30%; ;">
		<p style="text-align: center;">Plimsoll shoes, the predecessor of Sneakers</p>
		<p>Sneakers were first used by the Keds company, used to advertise shoes in 1900. Henry Nelson McKinney, an
			advertising agent for NWAyer & Son called sports shoes sneakers (sneakers) because of the soles. Sports
